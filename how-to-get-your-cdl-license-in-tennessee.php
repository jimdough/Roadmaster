<h1>How To Get Your CDL in Tennessee</h1>
<p>Want to get your <a href="cdl_training_class-a.php">Commercial Driver’s License</a> (CDL) in Tennessee? Looking to become a truck driver in your state? This is just the article to get you going in the right direction! While we can’t fit in all the details you need to know about getting your CDL in Tennessee, here are a few essential bits of information every future truck driver should know!</p>
<p>To start with, why do truck drivers need a CDL? Before the Commercial Motor Vehicle Act of 1986, any driver with a standard state driver’s license could drive a tractor-trailer. The new rules required truck drivers to be certified and tested with a state exam. Ultimately, this made the roads a safer place for everyone.</p>
<p>The <a href="https://www.fmcsa.dot.gov/">Federal Motor Carrier Safety Administration</a> (FMSCA) defines the CDL licenses as:</p>
<ul>
<li><strong>Class A CDL</strong> – Any combination of vehicles with a GVWR (Gross Vehicle Weight Rating) of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds. </li>
<li><strong>Class B CDL</strong> – Any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle not in excess of 10,000 pounds GVWR.</li>
</ul>
<p>If you eventually want to drive tractor-trailers across the country, you will need to earn your Class A CDL in Tennessee. </p>
<h2>CDL Requirements in Tennessee</h2>
<ul>
<li>Valid DOT Medical Card – Truck drivers have a lot of responsibility on their shoulders, so it’s very important that they’re healthy enough to make cross-country drives. For more information about the DOT Medical Exam, visit the <a href="https://www.fmcsa.dot.gov/medical/driver-medical-requirements/dot-medical-exam-and-commercial-motor-vehicle-certification">Medical Requirements for Drivers</a> page on the FMSCA website.</li>
<li>Proof of Social Security Number – You’ll have to show proof of a Social Security number (SSN) by showing some form of official document that states your SSN. Your SSN documents must be originals and copies will not be accepted whatsoever.</li>
<li>Identification – Forms of identity need to show your full name, including your first, middle, and last. They also need to show your date of birth to confirm the type of license you are eligible for. Drivers 18-20 years of age are permitted to drive intrastate (between states), and drivers 21 years or older are able to drive interstate (between states).</li>
<li>Proof of Tennessee Domicile – If you’re getting a CDL in Tennessee, you will be asked to bring verification that you live there. You will more than likely need to bring two forms of documentation to prove you are a state resident. The documents need to show your name and your permanent address.</li>
<li>Pass Your CDL Exam in Tennessee – This is very important and will ultimately will decide if you get the license or not. The test covers a mixture of behind-the-wheel driving and essential trucking facts. Make sure you come prepared to the test to avoid having to retake the whole thing over again. </li>
</ul>
