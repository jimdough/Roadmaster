<p>The <a href="http://www.trucking.org/ATA%20Docs/News%20and%20Information/Reports%20Trends%20and%20Statistics/10%206%2015%20ATAs%20Driver%20Shortage%20Report%202015.pdf">American Trucking Associations (ATA)</a> predict that if current trends last, there will be a shortage of nearly 175,000 truck drivers by 2024! Class A CDL trained truck drivers have a vital role in the economy and are responsible for distributing goods and necessities to all corners of the country. </p>
<h3>Why The Driver Shortage?</h3>
<p><strong>Improving Economy</strong> – America’s economy is finally seeing brighter days, and because so the country is increasing the production and distribution of its goods. This means a lot of positions are opening in the Professional Truck Driving Industry! It also means there is a growing surplus of goods that need to be delivered. </p>
<ul>
<li>The unemployment rate, reported by the <a href="http://www.bls.gov/news.release/pdf/empsit.pdf">Bureau of Labor Statistics</a> (BLS), is at its lowest point since 2008. More jobs means more spending, and more spending means more production. And it’s up to professional truck drivers to see those products distributed!</li>
</ul>
<ul>
<li>According to the <a href="http://www.trucking.org/ATA%20Docs/News%20and%20Information/Reports%20Trends%20and%20Statistics/10%206%2015%20ATAs%20Driver%20Shortage%20Report%202015.pdf">American Trucking Associations</a> (ATA), “68.9% of all freight tonnage is moved on the nation’s highways.” With the recent increase in production and distribution, the need for CDL trained truck drivers is high and rising.</li>
</ul>
<p><strong>Truck Driver Retirees</strong> – Retirement isn’t far away for many truck drivers and their absence could leave a substantial gap in the Industry. If the retiring drivers aren’t covered or replaced, ATA’s predictions could very well come true.</p>
<ul>
<li><span style="font-size: 12.96px; line-height: 18.144px;"><a href="http://www.trucking.org/ATA%20Docs/News%20and%20Information/Reports%20Trends%20and%20Statistics/10%206%2015%20ATAs%20Driver%20Shortage%20Report%202015.pdf">The ATA reports</a>, "The current average driver age in the OTR (Over-the-Road) TL (Truckload) industry is 49."  </span></li>
<li><span style="line-height: 18.144px;">Preceding generations of truck drivers are retiring faster than younger generations are taking over their roles. </span></li>
</ul>
