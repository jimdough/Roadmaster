<h1>How To Get Your CDL in North Carolina</h1>
<p>Are you looking to get your <a href="cdl_training_class-a.php">Commercial Driver’s License (CDL)</a> in North Carolina? That’s great news! Roadmaster has a <a href="truck_school_northcarolina.php">truck driving school in Dunn</a> with daily classes taught by industry experts! To get ahead of the game, here are some basic facts you should know and a few key requirements you can prepare for ahead of time:</p>
<p><em>What exactly is a CDL</em>? In the past, any driver could operate heavy machinery such as a bus or tractor-trailer with just a driver’s license. The Commercial Motor Vehicle Act of 1986 required drivers operating heavy machinery to get specific licenses depending on the weight of the vehicle. The <a href="https://www.fmcsa.dot.gov/">Federal Motor Carrier Safety Administration</a> defines them as:</p>
<ul>
<li><strong>Class A CDL</strong> – Any combination of vehicles with a GVWR (Gross Vehicle Weight Rating) of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds.</li>
<li><strong>Class B CDL</strong> – Any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle not in excess of 10,000 pounds GVWR.</li>
</ul>
<h2>CDL Requirements in North Carolina</h2>
<p>You’ll need to verify a few things before you can get your CDL in North Carolina:</p>
<ul>
<li><strong>Proof of Age and Identity</strong> – To get your CDL in North Carolina, you will need to bring <strong>two </strong>forms of proof to verify age and identity. The documents will need to have your <strong>full name (including your middle)</strong> and <strong>date of birth</strong>. <a href="http://www.ncdot.gov/dmv/examples/default.html#identity">Click here</a> to see the North Carolina DMV’s list of accepted identity documents.</li>
<li><strong>Proof of Social Security</strong> – You will be asked to bring an official document that verifies your Social Security number. <a href="http://www.ncdot.gov/dmv/examples/default.html#ssnumber">Click here</a> to find a list of DMV approved documents. All documents must be originals. Copies will not be accepted. </li>
<li><strong>Proof of Residency</strong> – Find a list of accepted proof of residency documents from the North Carolina DMV <a href="http://www.ncdot.gov/dmv/examples/default.html#residency">here</a>. If you aren’t a citizen of the United States, you will have to provide documentation from the U.S. government indicating legal presence.</li>
<li><strong>Pass Your CDL Test</strong> – This one’s a no brainer. You can’t get your CDL without passing both the written and the driving test in your state. You’ll want to come prepared to avoid having to retake the test, and that’s why <a href="http://www.roadmaster.com/cdl_training_class-a.php/">CDL training</a> is essential!</li>
</ul>
<p>Other required documents are updated periodically, and general requirements may change slightly with time. For the most detailed and up-to-date information, check the <a href="http://www.ncdot.gov/dmv/driver/commercial/">North Carolina DMV's website</a>.</p>
