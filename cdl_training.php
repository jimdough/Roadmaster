     <h3>Which type of CDL license do I need?</h3>
 
 <p> Since April of 1992, all drivers have been required to have a CDL in order to drive a Commercial Motor Vehicle. Tractor-trailers require advanced skills and knowledge above and beyond those required to drive a car or other lightweight vehicle.</p>
 
 <ol>
   <li><b>Class A CDL License:</b> The Class A is needed to operate any combination of vehicles with a <a href="http://en.wikipedia.org/wiki/GVWR" target="_blank">GVWR</a> of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds.</li>
     <ul>
       <li>Vehicles requiring a Class A CDL license are primarily tractor-trailers.</li>
     </ul>

      <li><b>Class B CDL License:</b> The Class B is needed to operate  any single vehicle with a GVWR of 26,001 or less pounds or any such vehicle towing a vehicle not in excess of 10,000 pounds. Vehicles requiring a Class B CDL license may include:</li>
     
     <ul>
       <li>Cars with trailers, buses (regardless of size), passenger vans and motorhomes
       <li>Standard-sized dump trucks, tow trucks, cement mixers and garbage trucks
       <li>Delivery trucks and utility trucks</li>
       <li>Any single vehicle with GVWR of 26,001 pounds or more that is not towing an excess of 10,000 pounds GVWR</li>
           </ul>
   </li></ol>
   
<h3>How do I get my Commercial Driver's License?</h3>

 
 <p>States are able to issue a CDL license only after a written and driving test have been given by the state or approved testing facility.</p>
 
 <p><b>It's important to train for your CDL exam for two reasons</b>:</p>
 <ol>
	 <li>The test itself can be extremely difficult to pass without the proper training.</li>
	 <li>Many carriers won't hire a candidate without proper training, even if you've passed the CDL exam.</li>
 </ol>	 

