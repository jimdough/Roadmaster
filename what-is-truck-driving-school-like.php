<p>Roadmaster is a hands-on truck-driving school and CDL training center with locations across the United States. Roadmaster provides you with the CDL training you need to get either a Class A or Class B CDL to help you start your Professional Truck Driving career.</p>
<p>With classes forming weekly, you can be on your way to an exciting new career in as little as <a href="blog/3-4-weeks-truck-driver-training-school-enough/">3-4 weeks!</a>! Roadmaster also offers free job placement assistance after graduation. Tuition assistance is also available for those who qualify.</p>
<p>Roadmaster Drivers School provides professional truck driver training that will prepare you for a career in the trucking industry. And how do we do that?</p>
<h3>How It Works</h3>
<p>Carl Matthews, a Roadmaster Training Director says:</p>
<p>"You're going to get intense training. You're going to get complete training for the trucking industry. We cover all aspects of everything that's needed for the trucking industry. When you leave here, you're fully prepared to go out and start with a company. And what we do here is we build a good foundation of a student that's never even been inside of a truck before. Start with classroom. We cover all the classroom curriculum, the logs, the recap. We cover federal motor carriers regulations. Different things inside the classroom, we prepare them for the permits. </p>
<p>"Half the class will be out on a back-up pad. The other half of the class will be on the road. So all the new guys will be in the classroom for the first, pretty much the first 40 hours of their training is in the classroom. But they're not always in the classroom. They're outside doing pre-trip inspection training. They're doing logbook training. We have a Pro- TREAD lab, a computer lab. They do some audio-visual interactive computer stuff. They do that. So there's a lot of stuff going on in that first 40 hours of school.</p>
<p>"You're going to be in a real truck. We have a 22-foot conventionals. We have 48- and 53-foot trailers. And by the end of the first week, they're actually driving, shifting, up-shifting, down-shifting, and driving tractor trailers. They go out, they drive on the highway each day, progressively getting more complicated, until the point that they're able to pass those tests at the end of the three weeks on their own." </p>
