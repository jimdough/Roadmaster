<h3>Still Wondering If A New Career As A CDL Professional Is Right For You?</h3>
<p> People from all walks of life have found their exciting new career at Roadmaster. </p>
<p>"There's all sorts of people that come in to see me. I've got people that have master's degrees. I have people that didn't graduate high school. We get everybody here. It's the most diverse community you will ever see. I just had a guy in here, and he was in construction. Now a lot of my students are former construction people that worked in that industry. Well construction, as you know, is up and down. So he would tell me, "Well, I'd worked three months, and I'd go on unemployment. Work another three months and go on unemployment." And a lot of why he was here, hey I don't think he just woke up one day and said "I want to be a truck driver." I think he was looking for something with good, consistent income that he can support his family with. And I think that's what brought him to Roadmaster." </p>
<p>In these uncertain economic times, Roadmaster provides you with the training you need to begin a stable career for you and your family. </p>
<p>"There is no such thing as a laid-off truck driver. Like I said, we have an excellent placement rate. Roadmaster in general, across the board, has an excellent placement rate. If they can get through the representative and get into school, we can usually work with anybody, and as long as they're willing to work with us and do the applications, they're going to go to work. They're going to go to work.</p>
