<p>In short, a <strong>Commercial Driver's License (CDL)</strong> shows carriers and employers that you are a certified and competent Professional Truck Driver.</p>
<p>Operating certain commercial motor vehicles requires specialized skills and knowledge. Prior to implementation of the Commercial Driver's License Program, anyone with a license to drive a car could also legally drive a tractor-trailer or a bus in a number of states. In circumstances where a classified licensing system was available, students weren't tested in vehicles similar to the kind they'd use. As a result, many drivers were operating motor vehicles that they may not have been qualified to drive.</p>
<h3>Why do I need a CDL?</h3>
<p>The Commercial Motor Vehicle Safety Act of 1986 was signed into law on October 27, 1986. The goal of the Act is to improve highway safety by ensuring that drivers of large trucks and buses are qualified to operate those vehicles and to remove unsafe and unqualified drivers from the highways.</p>
<p>The <a href="http://www.fhwa.dot.gov/">Federal Highway Administration (FHWA)</a> has developed and issued standards for testing and licensing commercial motor vehicle drivers. Among other things, the standards require states to issue a Class A CDL to a commercial motor vehicle driver only after the driver passes knowledge and skills tests administered by the state related to the type of vehicle to be operated. Drivers need a CDL license if they are in interstate, intrastate, or foreign commerce.</p>
<h3>What type of CDL do I need?</h3>
<p>While the type of license is dependent on the branch of work you're looking to pursue, Roadmaster courses are geared towards Class A and Class B CDLs, which are required of over-the-road (OTR) Professional Truck Drivers.</p>
