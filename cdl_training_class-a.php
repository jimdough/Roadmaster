<h3>How do a Class A CDL and a Class B CDL differ?</h3>
<p>According to the <a href="http://www.fmcsa.dot.gov/">Federal Motor Carrier Safety Administration</a>, the Federal standard requires states to issue a CDL (Commercial Drivers License) to drivers according to the following license classifications:</p>
<p><strong>Class A CDL Training</strong> - Any combination of vehicles with a GVWR of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds. Careers that require a Class A CDL include but are not limited to:</p>
<ul>
<li>Interstate Tractor Trailer Driver</li>
<li>Intrastate Tractor Trailer Driver </li>
</ul>
<p><strong>Class B CDL Training</strong> - Any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle not in excess of 10,000 pounds GVWR. Careers that require a <strong>Class B CDL License</strong> include but are not limited to:</p>
<ul>
<li>Supervisors</li>
<li>Heavy truck and tractor-trailer drivers</li>
<li>Hauling specialty cargo</li>
<li>Auto-transport drivers</li>
<li>Furniture delivery</li>
<li>Dispatcher</li>
<li>Self-employed truck driver</li>
<li>Local driver</li>
<li>Store delivery driver</li>
<li>Truck driver training instructor</li>
<li>Cement and concrete production</li>
<li>Courier</li>
<li>Bus driver</li>
<li>Supply company delivery</li>
</ul>
<p><strong>Roadmaster</strong> truck driving school offers Class A CDL training in all of its <a href="truck_school_locations.php">truck driving school locations</a>.</p>
<p><strong>Roadmaster</strong> Drivers School offers Class B CDL training and testing in all truck driving schools except San Antonio, Indianapolis, and North Carolina.</p>
<h3>How do I get my Commercial Driver's License?</h3>
<p>States are able to issue a CDL license only after a written and driving test have been given by the state or approved testing facility.</p>
<p><strong>It's important to train for your CDL exam for two reasons</strong>:</p>
<ul>
<li>The test itself can be extremely difficult to pass without the proper training.</li>
<li>Many carriers won't hire a candidate without proper training, even if you've passed the CDL exam.</li>
</ul>
