			 <h3>What can I do with a CDL license?</h3>
			 <p><b>Question: </b>Why do I need a <b>Class A CDL License</b>?</p>
			 <p><b>Answer:</b> It is widely recognized that driving certain commercial motor vehicles requires specialized skills and knowledge. Prior to implementation of the <b>Commercial Driver's License (CDL)</b> Program, in a number of states, any person licensed to drive an automobile could also legally drive a tractor-trailer or a bus. Even in many of the states that did have a classified licensing system, a person was not skills tested in a representative vehicle. As a result, many drivers were operating motor vehicles that they may not have been qualified to drive.</p>
			 <p>The Commercial Motor Vehicle Safety Act of 1986 was signed into law on October 27, 1986. The goal of the Act is to improve highway safety by ensuring that drivers of large trucks and buses are qualified to operate those vehicles and to remove unsafe and unqualified drivers from the highways. The <a href="http://www.fhwa.dot.gov/" target="_blank">Federal Highway Administration (FHWA)</a> has developed and issued standards for testing and licensing commercial motor vehicle drivers. Among other things, the standards require states to issue a Class A CDL License to a commercial motor vehicle driver only after the driver passes knowledge and skills tests administered by the state related to the type of vehicle to be operated. Drivers need a CDL license if they are in interstate, intrastate, or foreign commerce and drive a vehicle that meets one of the following definitions of a CMV: </p>
			 <div class="panel">
			   <p> <b>Class A CDL License required Commercial Motor Vehicle- </b>Any combination of vehicles with a GVWR of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds. </p>
			   <p><b> Class B CDL License required Commercial Motor Vehicle - </b>Any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle not in excess of 10,000 pounds GVWR. </p>
			 </div>
