<h1>How to Get Your CDL in Texas</h1>
<p>If you’re looking to train for a Commercial Driver’s License (CDL) in Texas, this is just the article for you! While all of the rules and suggestions on CDL training in Texas can’t fit into a single article, we can give you some of the basics!</p>
<p>For starters, what exactly is a CDL? In the past, any driver could operate heavy machinery such as a bus or tractor-trailer with their standard license. The Commercial Motor Vehicle Act of 1986 required drivers operating heavy machinery to get specific licenses depending on the weight of the vehicle. The <a href="https://www.fmcsa.dot.gov/">Federal Motor Carrier Safety Administration</a> defines them as:</p>
<ul>
<li><strong>Class A CDL</strong> – Any combination of vehicles with a GVWR (Gross Vehicle Weight Rating) of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds.</li>
<li><strong>Class B CDL</strong> – Any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle not in excess of 10,000 pounds GVWR.</li>
</ul>
<p>After drivers complete CDL training in Texas, they will test for their license either at <a href="truck_school_SANANTONIO.php">our facility</a> or at a local DMV/DOT. Class A CDL holders are eligible to drive the largest vehicles intrastate (in their state) and interstate (between states), taking positions with preferred carriers. Class B CDL holders can take up positions using lighter vehicles including bus driving, furniture delivery, cement and concrete production, and auto-transport driving to name a few.</p>
<h2>CDL Requirements in Texas</h2>
<ul>
<li><strong>Identification</strong> – You will need documentation to prove that you are either a <a href="https://www.txdps.state.tx.us/DriverLicense/LawfulStatusDLID.htm">U.S. citizen or have lawful presence</a>. In fact, you may be required to provide more than one document to verify identity. The information on all documents must match each other to be considered, so make sure the name, date of birth, or any other identifying facts match to avoid conflicts.</li>
<li><strong>Verification of Texas Residency</strong> – According to the Texas Department of Public Safety, “To apply for a CDL, individuals must provide two documents to verify their domicile is in Texas. The documents submitted must include the individual’s name and permanent address.” If you’re unable to provide two documents, you may be eligible to complete a <a href="http://www.txdps.state.tx.us/internetforms/Forms/DL-5.pdf">Texas Residency Affidavit</a>.</li>
<li><strong>Evidence of a SSN</strong> – Without proof of a Social Security Number, a CDL cannot be issued. Whatever proof you use to verify your SSN, it must be an unaltered original, not a copy. For a list of acceptable documents verifying your SSN in Texas,<a href="http://www.txdps.state.tx.us/internetforms/Forms/DL-57.pdf"> click here</a>.</li>
<li><strong>Successful Completion of the CDL Exam</strong> – The “Supplement Application Texas Commercial Driver License Certifications and Record of CDL Examination” is the test that will determine whether or not you get the license. It’s important that you arrive well prepared for this exam.</li>
</ul>
