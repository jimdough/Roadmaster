<p>Trainees pay tuition fees for their <a href="cdl_training_class-a.php">Class A CDL</a> courses up front, and are then free to choose a carrier that offers tuition reimbursement. If paying the full cost of tuition isn’t an option, Roadmaster offers financial assistance to those who qualify. For more information, visit our <a href="truck_school_finance.php">Finance page.</a></p>
<h2>Apply to a Carrier Through Roadmaster’s Job Placement Assistance</h2>
<p>Roadmaster’s <a href="job-placement.php">Job Placement assistance</a> works alongside students and teaches them essential skills when applying to trucking jobs. Trainees can select from a variety of major carriers to find a job that best fits their lifestyle and preferred benefits. Job Placement begins on day one and is a resource that can be used by all Roadmaster graduates anytime in the future! </p>
<h2>Sign-on With a Carrier That Meets Your Tuition Reimbursement Preferences</h2>
<p>It’s a good idea to consider the amount of tuition reimbursement a carrier offers when choosing your future company! Some of our partner carriers offer<strong> up to 90% tuition reimbursement to new hires</strong>. The reimbursement amount varies from carrier to carrier, and should be carefully looked at when selecting your applications. Roadmaster instructors are there help you locate information and make the best decisions as you look through carrier applications.</p>
<h2>Repayment Conditions</h2>
<p>Tuition reimbursement is typically broken down over a series of monthly payments. The amount of the reimbursement is distributed over time to make sure drivers are serious about working for their new company. In most cases the carrier will deduct the tuition reimbursement directly from your check so that you don't have to. In addition to the monthly tuition reimbursement, drivers will earn things like</p>
