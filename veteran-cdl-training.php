<h3>What Is A Veteran CDL Skills Test Waiver</h3>
<p>A veteran CDL skills test waiver is basically a way for former military drivers to prove that they’ve had enough practical skills training in the form of driving comparable vehicles while serving in the military. Those who qualify for the waiver are able to skip the skills testing that many civilians would need to successfully complete before earning a Class A CDL.</p>
<p>According to the <a href="https://www.fmcsa.dot.gov/registration/commercial-drivers-license/military">Federal Motor Carrier Safety Administration’s (FMCSA) website:</a></p>
<blockquote>“In May 2011, the Federal Motor Carrier Safety Administration finalized the Commercial Learner’s Permit rule. Within that rule is a provision that gives State Driver Licensing Agencies (SDLAs) the authority to substitute two years of CMV safe driving experience in military equivalents of commercial motor vehicles, for the skills test portion of the commercial driver license skills test. Military drivers must apply within one year of separation from active duty."</blockquote>
<p>So, basically, if you drove certain military vehicles and meet a few other requirements, you can get your Class A CDL license sooner than later. What are those other requirements? Well… </p>
<h3>Qualifications For A Veteran CDL Skills Test Waiver</h3>
<p>First, you should make sure your state offers the military skills test waiver. You can do that by <a href="https://www.fmcsa.dot.gov/sites/fmcsa.dot.gov/files/docs/Military-Skills-Test-Map-06-25-14.pdf">clicking here</a> and checking the map page on the FMCSA’s website.</p>
<p>The FMSCA's website also explains:</p>
<blockquote>"The regulation, 49 CFR 383.77, provides guidance for SDLAs that requires the applicant to certify:
<ul>
<li>His/her safe driving experience;</li>
<li>That he or she has not held more than one license (except a military DL) in the past two years;</li>
<li>Has not had his/her base State issued driver license suspended, revoked or cancelled; and,</li>
<li>Has not had convictions in any type of motor vehicle for the disqualifying CDL offenses listed elsewhere in the regulations.</li>
</ul>
<p>There are also other requirements that States must recognize in order to take advantage of this Skills Test Waiver, but the process allows States to assist veterans and active duty personnel in their transition from their military occupation to a civilian career. As more and more of our military troops return home from active duty, the Department of Transportation and FMCSA have joined with the Departments of Defense, Labor, and Veterans Affairs to ease this process.”</p>
</blockquote>
<p>Once you’ve made sure your state offers the waiver and are ready to show that you meet all the necessary requirements, including the above qualifications, the next step is to get the ball rolling by filling out the official application form for the waiver, which you can get by <a href="https://www.fmcsa.dot.gov/sites/fmcsa.dot.gov/files/docs/7-1-2014-FINAL-APPLICATION-FOR-MILITARY-SKILLS-TEST-WAIVER-5-15-2012.pdf">clicking here</a>.</p>
<p> If you have any questions, please be sure to check with your State Driver Licensing Agency.</p>
