<h2>What is a Pre-Hire Letter?</h2>
<p>Many students at Roadmaster Drivers School receive a pre-hire letter during job placement.  A pre-hire letter comes from a carrier and shows that the company is interested in hiring them as a driver. So how does a student get a pre-hire letter and what does it mean for them?</p>
<h6>How Do I Get a Pre-Hire Letter?</h6>
<p>Students enrolled in Roadmaster's Class A CDL training program get an opportunity to meet recruiters from many of the nation's top OTR carriers. Often, this happens as early as the first week of training. Students have already been through Roadmaster's  qualification process by this point and know if they qualify for a pre-hire through the job placement program.  Here are a few of the areas carriers are looking at:</p>
<ul>
<ul>
<li>Driving record</li>
<li>Criminal background</li>
<li>Work history</li>
</ul>
</ul>
