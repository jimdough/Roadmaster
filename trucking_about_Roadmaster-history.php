	  		<h3>The history of Roadmaster trucking school</h3>
<ul>
   <li><strong>May 1992:</strong> Roadmaster Drivers School opened its first school  at the Florida State Fairgrounds location in <a href="truck_school_TAMPA.php">Tampa, Florida</a>. During its  first year of operation, the company obtained a license to perform as a  Third Party Commercial Driver Licensing Examiner for the State of  Florida. </li>
 
 <li><strong>October 1995:</strong> Roadmaster Drivers School began evaluating  sites for a second school. After conducting various demographic  studies, <a href="truck_school_OHIO.php">Columbus, Ohio</a> was chosen as the location of the company's  second school.</li>
 
 <li><strong>November 1997:</strong> Roadmaster Drivers School opens a training school in <a href="truck_school_JACKSONVILLE.php">Jacksonville, Florida</a>.</li>

<li><strong>August 1998:</strong> Again, continuing the expansion efforts,  Commercial Driver Training of Augusta, Georgia is purchased to offer  educational training and Commercial Drivers Licenses testing.</li>

<li><strong>November 1999:</strong> Diesel Institute of America was purchased in Tampa, Florida. Along with providing educational training and Commercial Drivers Licenses testing, Diesel Institute provides  training of automotive and diesel technicians.</li>

 <li><strong>July 2002:</strong><a href="truck_school_SaltLake.php"> Roadmaster Drivers School of Salt Lake City, Inc</a>. began to offer its first classes.</li>

<li><strong>September 2002:</strong> <a href="truck_school_SANANTONIO.php">Roadmaster Drivers School of San Antonio, Inc.</a> began school operations.</li>

<li><strong>September 2004:</strong> Roadmaster Drivers School became a training  partner with Mid South Community College and opened a commercial truck  driver program on the campus in <a href="truck_school_WESTMEMPHIS.php">West Memphis, Arkansas</a>.</li>

<li><strong>September 2005:</strong> Roadmaster Drivers School volunteered 3 trailers and 2 tractors to bring water into New Orleans after Hurricane Katrina. Roadmaster staff and other volunteers generously drove the trucks from Tampa to New Orleans stopping in Zephyrhills to pick up the fresh water. </li>
 
 <li><strong>October 2005:</strong> Roadmaster Drivers School opened a commercial  truck driver program in Columbus, Indiana. The school was relocated to  <a href="truck_school_INDIANA.php">Indianapolis</a> in January 2007.</li>

<li><strong>August 2007:</strong> Roadmaster Drivers School acquired Interstate Driving Academy in <a href="truck_school_northcarolina.php">Dunn, North Carolina</a>.</li>

<li><strong>March 2009:</strong> Roadmaster Drivers School opened school locations in Oklahoma and Missouri.</li>

<li><strong>December 2009:</strong> Roadmaster Drivers School opened their first west coast location in <a href="truck_school_fontanaCalifornia.php">Fontana, California</a>.</li>

<li><strong>November 2010:</strong> <a href="http://www.atstrainingsystems.com/">Advance Training Systems LLC</a> a consortium of expert organizations developed an advanced training system and simulator beginning in October of 2008. ATS sells its first group of simulators to the industry.</li>

<li><strong>January 2011:</strong> Roadmaster Drivers School opened its first school location in Tennessee in Chattanooga.</li>

<li><strong>March 2011:</strong> eTREAD, the online version of classroom training is made available online for students.</li>

<li><strong>February 2015:</strong>Roadmaster Drivers School opened its new school in Bethlehem, Pennsylvania.</li>

</ul>

