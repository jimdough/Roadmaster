<h1>How to Get Your CDL in Florida</h1>
<p>Do you want to know how to get a Class A CDL License in Florida? Roadmaster Drivers School operates three schools in <a href="truck_school_JACKSONVILLE.php">Jacksonville</a>, <a href="truck_school_ORLANDO.php">Orlando</a>, and <a href="truck_school_TAMPA.php">Tampa</a> and has trained thousands of professional truck drivers. Let’s go over some of the basic things you need to get started!</p>
<h3>What exactly is a CDL and why do you need it?</h3>
<p>In short, the CDL is a certification that qualifies you to operate heavy machinery.There are a few types of CDL’s; depending on the total weight of the vehicle you will be driving. The <a href="https://www.fmcsa.dot.gov/">Federal Motor Carrier Safety Administration</a> (FMSCA) defines Class A and Class B CDL’s as:</p>
<ul>
<li>Class A CDL – Any combination of vehicles with a GVWR (Gross Vehicle Weight Rating) of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds. This is the one you need to work for major carriers like the ones that Roadmaster works with. (Link to carrier choice and list carriers)</li>
<li>Class B CDL – Any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle not in excess of 10,000 pounds GVWR.</li>
</ul>
<p>The Commercial Motor Vehicle Act of 1986 requires drivers to take a state exam that proves their skill and knowledge on the road when operating heavy machinery. CDL training is all about keeping the roads safe!</p>
<h3>CDL Requirements in Florida</h3>
<p>You can make sure that you’re ready to get your CDL by doing or providing the <a href="https://www.flhsmv.gov/resources/handbooks-manuals/">following</a>:</p>
<ul>
<li>Proof of Identification – All applicants for a Class E license (required for a Class A CDL in Florida) are required to bring proof of identification. Florida requires an original birth certificate or US passport. If you don’t have an original birth certificate, you can substitute with a certified copy.</li>
<li>Social Security Number Verification – Proof of a Social Security number is required in order to receive your CDL license. As with other proofs of identification, your evidence of SSN must be an original and cannot be a copy. The state of Florida will accept either your Social Security Card or a W-2.</li>
<li>Proof of Residential Address – To be eligible for a Florida CDL, you should be a resident of Florida. To prove this, you can show documents such as utility, cable, or telephone bills with your address listed.</li>
<li>Pass the CDL exam – This is the last (and arguably most important!) step in getting your CDL. There are a couple parts to the exam, including general knowledge, combinations, pre-trip inspection, and the road test. This covers both written and practical on-the-road material.</li>
</ul>
