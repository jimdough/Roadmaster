<h1>How To Get Your CDL License in Pennsylvania</h1>
<p>Thinking about earning your <a href="cdl_training_class-a.php">Commercial Driver’s License</a> (CDL) in Pennsylvania? You’ve come to the right place! While we can’t fit in all the details and suggestions for earning your Class A CDL in this article, we can start you off in the right direction.</p>
<p>What is a CDL after all, and why do we use the term “Class A?” In the past, any driver could operate a tractor-trailer and carry freight cross-country with just a regular driver’s license. The Commercial Motor Vehicle Act of 1986 changed that by requiring truck drivers to be certified with a CDL. The <a href="Federal%20Motor%20Carrier%20Safety%20Administration">Federal Motor Carrier Safety Administration</a> defines them as:</p>
<ul>
<li><strong>Class A CDL</strong> – Any combination of vehicles with a GVWR (Gross Vehicle Weight Rating) of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds.</li>
<li><strong>Class B CDL</strong> – Any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle not in excess of 10,000 pounds GVWR.</li>
</ul>
<p>If you’re looking to become a truck driver moving freight across the nation, the Class A CDL is the license you will need to get. Class B CDL holders take up positions driving lighter vehicles such as bus driving, furniture delivery, cement production, and auto-transport driving. </p>
<h2>CDL Requirements in Pennsylvania</h2>
<ul>
<li><strong>Identification</strong> – You will be asked to provide proof that you are either U.S. citizen or else have lawful presence. Your identification must have your full name on it (including first, middle, and last) and must state your age. Drivers 18-20 are only permitted to drive intrastate (within the same state) while drivers 21 and older are permitted to drive interstate (between states).</li>
<li><strong>Proof of Pennsylvania Residency</strong> – In order to get your Class A CDL in Pennsylvania, you’ll need to prove that you are a resident of the state. The CDL exam is a state-level exam and licenses are issued through the state.</li>
<li><strong>Evidence of a SSN</strong> – You will be asked to provide proof of a Social Security number by presenting official documents stating it. All SSN documents must be originals and copies will not be accepted. </li>
<li><strong>Passed CDL Exam</strong> – This is the last stage before earning your CDL exam! It’s very important that you come prepared to answer trucking questions and demonstrate driving skills in a truck on the road. </li>
</ul>
<p>Requirements change and update over time, so for the most up-to-date information check with your state DMV or DOT, or <a href="https://secure.livechatinc.com/licence/4004091/open_chat.cgi">chat with a Roadmaster representative</a>.</p>
