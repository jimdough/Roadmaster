 <div class="small-12 medium-12 large-12 columns editable" id="row-col1"><h1>How To Get Your CDL in California</h1>
<p>If CDL training is on your radar in California, train with Roadmaster to excel in your career in the truck driving industry! Roadmaster has trained thousands of professional truck drivers across the nation and has a California <a href="truck_school_fontanaCalifornia.php">truck driving school location in Fontana</a>. To stay ahead of the game, here are some things you need to know about getting your CDL in California:</p>
<p><a href="cdl_training_class-a.php/">What exactly is a CDL</a> and why do you need it? In short, the CDL is a certification that qualifies you to drive tractor-trailers on the nation’s highways. Before the CDL was created, anyone could drive freight on the highways with only a driver’s license. It wasn’t very safe, but now <a href="what-is-cdl-training.php">CDL training</a> helps protect everyone on the road!</p>
<p>The Commercial Motor Vehicle Act of 1986 requires drivers to take a state CDL test that proves their skill and knowledge on the road when operating heavy machinery. There are a few types of CDL’s, depending on the total weight of the vehicle you will be driving. The <a href="https://www.fmcsa.dot.gov/">Federal Motor Carrier Safety Administration</a> (FMSCA) defines Class A and Class B CDL’s as:</p>
<ul>
<li><strong>Class A CDL</strong> – Any combination of vehicles with a GVWR (Gross Vehicle Weight Rating) of 26,001 or more pounds provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds. </li>
<li><strong>Class B CDL</strong> – Any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle not in excess of 10,000 pounds GVWR.</li>
</ul>
