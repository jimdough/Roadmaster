<h2>CDL Requirements in California</h2>
<p>To get your CDL in California, you’ll need to provide proof of a few documents. Your documents will need to verify:</p>
<ul>
<li><strong>Proof of Identification</strong> – Items like driver’s licenses and identification cards verify your name, but they also verify your age. According to national standards, drivers must be 21 years old to drive interstate (between states) and 18 to drive intrastate (within the same state). </li>
<li><strong>Medical Examination Report</strong> – A medical examination report may be required, depending on the last time you had one performed. A medical examination report from the California DMV <a href="https://www.dmv.ca.gov/portal/wcm/connect/fa2a514f-130a-4935-adba-e55ec36e234a/dl51.pdf?MOD=AJPERES">can be found here</a>.</li>
<li><strong>Social Security</strong> – Proof of a Social Security number is required in order to get your CDL. As with other proofs of identification, the Social Security documents you bring must be originals and cannot be copies. </li>
<li><strong>Passed CDL Test</strong> – This is what will determine your eligibility to work as a truck driver in the end, so it’s very important! The test covers a mixture of knowledge-based questions and a behind-the-wheel driving exam. Make sure you’re thoroughly prepared the first time around to avoid having to retake the test.</li>
</ul>
<p>Requirements are always subject to change. To make sure that your documentation requirements are up-to-date, check the <a href="http://www.dmv.org/ca-california/apply-cdl.php">California Department of Motor Vehicles</a>.</p>
