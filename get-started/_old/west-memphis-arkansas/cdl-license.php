<h2>CDL Requirements in Arkansas</h2>
<p>In order to receive your CDL in Arkansas you will need to provide proof of:</p>
<ul>
<li><strong>Age – </strong>You will need to bring a document that verifies your date of birth. According to nationwide standards, drivers who are under 21 can only drive intrastate (within the same state) while drivers who are 21 or over are able to drive interstate (between states).</li>
<li><strong>Identity – </strong>In order to get your CDL, you will need to show proof of your identity by providing evidence of a Social Security number. You can find your identification numbers on documents such as Social Security cards and IRS information (W-2, W-4, 1099, etc.).</li>
<li><strong>Written and Skills Test (CDL Exam) – </strong>This is the big one! To get your CDL you’ll need to pass the state test. The CDL exam will cover a mixture of knowledge-based testing as well as hands-on testing scenarios in a truck. This will be your opportunity to show a prompter all the skill you’ve learned at Roadmaster Truck Driving School!</li>
</ul>
<p>If this seems like a lot of information, don’t worry! Roadmasters’ Admissions Representatives will help you get started and on your way to a professional truck driving career in no time!</p>
<p>Requirements are always subject to change. To make sure that your state documentation requirements are up-to-date, check the <a href="http://www.dmv.org/ar-arkansas/apply-cdl.php">Arkansas DMV website</a>.</p>
